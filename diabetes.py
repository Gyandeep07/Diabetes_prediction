y=diabetes['diabetes']
x=diabetes[['pregnancies', 'glucose', 'diastolic', 'triceps', 'insulin', 'bmi',
       'dpf', 'age',]]

from sklearn.model_selection import train_test_split
x_train,x_test,y_train,y_test=train_test_split(x,y,random_state=2529)

from sklearn.linear_model import LogisticRegression
model=LogisticRegression(max_iter=500)

model.fit(x_train,y_train)

y_predict=model.predict(x_test)

from sklearn.metrics import accuracy_score

accuracy_score(y_test,y_predict)

new_diabetes=pd.DataFrame([[1,	89,	66,	23	,94	,28.1,	0.167	,21]],['pregnancies', 'glucose', 'diastolic', 'triceps', 'insulin', 'bmi',
       'dpf', 'age',])

new_prediction=model.predict(new_diabetes)

